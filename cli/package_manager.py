# =============================================================
# AION Package Manager — CLI Interface
# =============================================================
# Handles all package management commands:
#   python main.py --install <package>
#   python main.py --uninstall <package>
#   python main.py --packages
#
# Architecture:
#   - Looks up package in BUILTIN_PACKAGES registry
#   - Installs it by creating a module file in packages/
#   - Updates registry.json to track installed packages
#   - Makes it available via "use <package>" in AION code

import os
import json
from config import Color
from cli import print_success, print_error, print_info, print_warning
from packages import (
    BUILTIN_PACKAGES, load_registry, save_registry,
    is_installed, PACKAGES_DIR
)


def install_package(name: str) -> bool:
    """
    Install a package by name.
    Returns True if successful, False otherwise.
    """
    print_info(f"Installing '{name}'...")

    # Check if already installed
    if is_installed(name):
        print_warning(f"'{name}' is already installed.")
        return True

    # Check if package exists
    if name not in BUILTIN_PACKAGES:
        available = ", ".join(BUILTIN_PACKAGES.keys())
        print_error(
            f"Package '{name}' was not found.\n"
            f"  Available packages: {available}"
        )
        return False

    package = BUILTIN_PACKAGES[name]

    # Generate the package module file
    success = _create_package_module(name, package)
    if not success:
        return False

    # Update registry
    registry = load_registry()
    registry[name] = {
        "name":        package["name"],
        "version":     package["version"],
        "description": package["description"],
        "installed":   True,
    }
    save_registry(registry)

    print_success(
        f"Installed '{name}' v{package['version']} — "
        f"{package['description']}"
    )
    print_info(f"Use it in AION with:  use {name}")
    return True


def uninstall_package(name: str) -> bool:
    """
    Uninstall a package by name.
    Returns True if successful, False otherwise.
    """
    if not is_installed(name):
        print_error(f"'{name}' is not installed.")
        return False

    # Remove module file
    module_path = os.path.join(PACKAGES_DIR, f"{name}.py")
    if os.path.exists(module_path):
        os.remove(module_path)

    # Update registry
    registry = load_registry()
    del registry[name]
    save_registry(registry)

    print_success(f"Uninstalled '{name}' successfully.")
    return True


def list_packages():
    """Display all available and installed packages."""
    installed = load_registry()
    available = BUILTIN_PACKAGES

    print()
    print(f"{Color.CYAN}{Color.BOLD}  AION Package Registry{Color.RESET}")
    print(f"  {Color.DIM}{'─' * 44}{Color.RESET}")

    for name, info in available.items():
        is_inst  = name in installed
        status   = (f"{Color.GREEN}✓ installed{Color.RESET}"
                    if is_inst else
                    f"{Color.DIM}  available{Color.RESET}")
        version  = info["version"]
        desc     = info["description"]

        print(f"  {Color.BOLD}{name:<16}{Color.RESET}"
              f"{Color.DIM}v{version:<8}{Color.RESET}"
              f"{status}")
        print(f"  {Color.DIM}{'':16}{desc}{Color.RESET}")
        print()

    total     = len(available)
    inst_count = len(installed)
    print(f"  {Color.DIM}{inst_count}/{total} packages installed{Color.RESET}")
    print()


def _create_package_module(name: str, package: dict) -> bool:
    """
    Generate a Python module file for the package
    inside the packages/ directory.
    """
    module_path = os.path.join(PACKAGES_DIR, f"{name}.py")

    try:
        content = _generate_module_code(name, package)
        with open(module_path, "w", encoding="utf-8") as f:
            f.write(content)
        return True

    except Exception as e:
        print_error(f"Failed to create package module: {e}")
        return False


def _generate_module_code(name: str, package: dict) -> str:
    """Generate the Python code for a package module."""

    generators = {
        "charts":     _charts_module,
        "auth":       _auth_module,
        "validation": _validation_module,
        "colors":     _colors_module,
        "random":     _random_module,
    }

    generator = generators.get(name)
    if generator:
        return generator()

    # Generic fallback
    return f'''# AION Package — {name}
# Auto-generated by AION Package Manager

def load() -> dict:
    return {{}}
'''


